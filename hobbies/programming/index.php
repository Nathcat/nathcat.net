                </p>

                <p>
                    As mentioned in the documentation, I targeted Android 12, the highest available version, for this project. However, during development, Google released
                    Android 13, which happened to implement changes to background services which meant that the <i>Networker service</i>, effectively the backbone of the entire
                    application, no longer ran while the application was closed, which was essential for things like notifications to appear when messages were received while the application
                    was closed. I also wasn't particularly happy with the way I had implemented the UI.
                </p>

                <p>
                    As a result, once I had finished my coursework, accepting the project despite its flaws, I started to redevelop the client application to:
